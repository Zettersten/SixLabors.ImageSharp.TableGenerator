namespace BlazorFast.ImageSharp.TableGenerator.Extensions;

/// <summary>
/// Specifies the visual theme mode for table rendering.
/// </summary>
public enum ThemeMode
{
    /// <summary>
    /// Light theme with white background and dark text.
    /// </summary>
    Light,

    /// <summary>
    /// Dark theme with dark background and light text.
    /// </summary>
    Dark,

    /// <summary>
    /// Minimal theme with no borders and clean spacing.
    /// </summary>
    Minimal,

    /// <summary>
    /// Compact theme with reduced padding and smaller font.
    /// </summary>
    Compact,
}
